//! Модульный WebRTC Signaling Server на Rust.
//!
//! # Архитектура
//!
//! Проект следует принципам Clean Architecture и SOLID:
//!
//! - [`domain`] — Сущности и бизнес-правила (чистый Rust, без зависимостей от фреймворков).
//! - [`handlers`] — Обработка команд и событий (применение доменной логики).
//! - [`infrastructure`] — Адаптеры к внешним системам (хранилища, БД, реестры подключений).
//! - [`transport`] — Работа с протоколами (WebSocket, разбиение потоков).
//! - [`orchestrator`] — Управление жизненным циклом соединений (связывает все слои).
//!
//! # Строгая инкапсуляция
//!
//! Все модули объявлены как `mod` (приватные). Мы намеренно скрываем внутреннюю структуру,
//! чтобы:
//! 1. Контролировать публичный API (пользователь видит только то, что мы разрешаем).
//! 2. Иметь свободу рефакторить внутренности без поломки совместимости (Semantic Versioning).
//! 3. Следовать принципу "Tell, don't ask" — объекты сами управляют своим состоянием.
//!
//! # Быстрый старт
//!
//! ```rust
//! use signaling_server::{Config, Orchestrator, MemoryRoomStore, ConnectionRegistry};
//!
//! # async fn example() -> Result<(), signaling_server::AppError> {
//! // 1. Загружаем конфигурацию из переменных окружения
//! let config = Config::from_env()?;
//!
//! // 2. Создаём хранилище (комнаты) и реестр (сокеты)
//! let store = MemoryRoomStore::new();
//! let registry = ConnectionRegistry::new();
//!
//! // 3. Инициализируем оркестратор
//! let orchestrator = Orchestrator::new(store, registry);
//!
//! // 4. Запускаем сервер (в main.rs через Axum)
//! // orchestrator.handle_connection(socket).await;
//! # Ok(())
//! # }
//! ```

// ─────────────────────────────────────────────────────────────
// ВНУТРЕННИЕ МОДУЛИ (приватные)
// ─────────────────────────────────────────────────────────────
// ▶ Эти модули НЕ видны снаружи библиотеки.
// ▶ Доступ к ним имеют только другие модули внутри этого крейта.
// ▶ Это гарантирует, что внешние зависимости не будут лезть в детали реализации.

mod config;
mod error;
mod domain;
mod handlers;
mod infrastructure;
mod transport;
mod orchestrator;

// ─────────────────────────────────────────────────────────────
// ПУБЛИЧНЫЙ API (реэкспорты)
// ─────────────────────────────────────────────────────────────
// ▶ Здесь мы явно указываем, что доступно пользователям библиотеки и `main.rs`.
// ▶ Если типа нет в этом списке — он считается внутренней реализацией.

// ─────────────────────────────────────────────────────────────
// 1. Конфигурация и Ошибки (Базовые типы)
// ─────────────────────────────────────────────────────────────

/// Конфигурация сервера.
///
/// Загружается из переменных окружения (через `dotenvy` и `envy`).
/// Содержит настройки порта, хоста, логирования и других параметров.
pub use config::Config;

/// Глобальный тип ошибки приложения.
///
/// Объединяет ошибки всех слоёв архитектуры через `thiserror`.
/// Позволяет использовать оператор `?` в любой функции, возвращающей `AppResult`.
pub use error::AppError;

/// Тип-алиас для `Result<T, AppError>`.
///
/// Делает сигнатуры функций короче и единообразными.
pub use error::AppResult;

// ─────────────────────────────────────────────────────────────
// 2. Domain: Публичные DTO и Сообщения
// ─────────────────────────────────────────────────────────────
// ▶ Экспортируем только то, что нужно для сериализации (JSON) 
//   и публичного API (информация об участнике).
// ▶ Сами сущности (Room, Participant) скрыты, так как они управляются хранилищем.

/// Сообщение от клиента к серверу.
///
/// Используется для десериализации входящих WebSocket-сообщений.
/// Поддерживает команды управления комнатами и WebRTC-сигналы.
///
/// # Формат JSON
///
/// ```json
/// { "type": "join_room", "room_id": "room-123" }
/// ```
pub use domain::ClientMessage;

/// Сообщение от сервера к клиенту.
///
/// Используется для сериализации ответов и событий.
/// Включает подтверждения, ошибки и ретрансляцию WebRTC-данных.
pub use domain::ServerMessage;

/// Информация об участнике комнаты.
///
/// DTO (Data Transfer Object), который передается клиентам.
/// Содержит только публичные данные (ID, имя), без каналов связи.
pub use domain::ParticipantInfo;

// ─────────────────────────────────────────────────────────────
// 3. Infrastructure: Абстракции и Реализации
// ─────────────────────────────────────────────────────────────
// ▶ Здесь мы даем доступ к интерфейсам хранения и управления подключением.
// ▶ Это позволяет `main.rs` создавать и конфигурировать эти компоненты.

/// Трейт репозитория комнат.
///
/// Определяет интерфейс для хранения и получения данных о комнатах.
/// Позволяет заменять реализацию хранилища (например, на Redis) без изменения бизнес-логики.
pub use infrastructure::RoomRepository;

/// In-memory реализация [`RoomRepository`].
///
/// Использует `DashMap` для потокобезопасного хранения данных в памяти.
/// Подходит для разработки и небольших продакшен-развёртываний.
pub use infrastructure::MemoryRoomStore;

/// Реестр активных соединений.
///
/// Управляет маппингом `ParticipantId -> WebSocketSender`.
/// Позволяет отправлять сообщения участникам, не храня `sender` внутри сущностей домена.
/// Это ключевой компонент для соблюдения чистоты Домена (Domain).
pub use infrastructure::ConnectionRegistry;

// ─────────────────────────────────────────────────────────────
// 4. Orchestrator: Главный компонент
// ─────────────────────────────────────────────────────────────

/// Оркестратор соединений.
///
/// Координирует обработку WebSocket-подключений:
/// 1. Принимает входящие сообщения.
/// 2. Маршрутизирует их к соответствующим обработчикам (через `handlers`).
/// 3. Управляет состоянием соединения (рукопожатие, в комнате, закрыто).
/// 4. Гарантирует очистку ресурсов (удаление из комнаты) при разрыве связи.
///
/// Параметризован трейтом [`RoomRepository`], что обеспечивает инверсию зависимостей (DIP).
pub use orchestrator::Orchestrator;

// ─────────────────────────────────────────────────────────────
// ВНУТРЕННИЕ ТИПЫ (не реэкспортируются)
// ─────────────────────────────────────────────────────────────
// ▶ Эти типы доступны только внутри crate.
// ▶ Пользователи библиотеки работают с ними через публичные интерфейсы.
//
// Примеры скрытых типов:
// - domain::room::Room          // Внутренняя логика комнаты (приватна)
// - domain::participant::Participant // Внутренняя сущность (приватна)
// - handlers::room::handle_create_room // Детали обработки (приватны)
// - transport::websocket::split_socket // Утилита транспорта (приватна, используется оркестратором)
//
// Если в будущем понадобится сделать какой-то из них публичным —
// просто добавьте соответствующий `pub use` выше.
