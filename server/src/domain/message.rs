//! Сообщения протокола сигнализации.
//!
//! Этот модуль определяет структуры данных для обмена между клиентом и сервером.
//! Все типы реализуют [`Serialize`] и [`Deserialize`] для автоматической
//! конвертации в/из JSON.
//!
//! # Формат сообщений
//!
//! Сообщения используют **тагированный enum** через `#[serde(tag = "type")]`:
//!
//! ```json
//! {
//!   "type": "join_room",
//!   "room_id": "room-123",
//!   "display_name": "Alice"
//! }
//! ```
//!
//! Это позволяет легко маршрутизировать сообщения на стороне сервера и клиента.

use serde::{Deserialize, Serialize};

/// Сообщение от клиента к серверу.
///
/// # Жизненный цикл соединения
///
/// 1. Клиент отправляет [`CreateRoom`] или [`JoinRoom`] (рукопожатие)
/// 2. После успешного присоединения можно отправлять [`Offer`], [`Answer`], [`IceCandidate`]
/// 3. [`LeaveRoom`] завершает сессию (опционально, сервер также обрабатывает разрыв соединения)
///
/// # Примеры JSON
///
/// ```json
/// {"type": "create_room"}
/// {"type": "join_room", "room_id": "abc", "display_name": "Bob"}
/// {"type": "offer", "target_id": "user-2", "sdp": "v=0\r\no=-..."}
/// ```
#[derive(Debug, Clone, Serialize, Deserialize)]
#[serde(tag = "type", rename_all = "snake_case")]
pub enum ClientMessage {
    /// Создать новую комнату и присоединиться к ней как создатель.
    ///
    /// Сервер генерирует уникальный `room_id` и `participant_id`,
    /// возвращает их в [`ServerMessage::RoomCreated`].
    CreateRoom,

    /// Присоединиться к существующей комнате.
    ///
    /// # Поля
    ///
    /// - `room_id`: идентификатор целевой комнаты (обязателен)
    /// - `display_name`: отображаемое имя участника (опционально, по умолчанию "Anonymous")
    JoinRoom {
        /// Идентификатор комнаты для присоединения.
        room_id: String,
        /// Отображаемое имя участника. Если не указано — используется значение по умолчанию.
        #[serde(default = "default_display_name")]
        display_name: String,
    },

    /// Покинуть комнату (явное завершение сессии).
    ///
    /// Сервер удалит участника из списка и уведомит остальных через
    /// [`ServerMessage::ParticipantLeft`].
    LeaveRoom,

    /// WebRTC Offer: предложение установить соединение.
    ///
    /// Отправляется инициатором соединения целевому участнику.
    /// Содержит SDP-описание медиа-параметров.
    Offer {
        /// Идентификатор участника-получателя.
        target_id: String,
        /// SDP-описание в текстовом формате.
        sdp: String,
    },

    /// WebRTC Answer: ответ на предложение.
    ///
    /// Отправляется получателем `Offer` в ответ инициатору.
    Answer {
        /// Идентификатор участника-получателя (инициатора).
        target_id: String,
        /// SDP-описание в текстовом формате.
        sdp: String,
    },

    /// WebRTC ICE Candidate: кандидат для установления соединения.
    ///
    /// Может отправляться многократно в процессе ICE-сбора.
    IceCandidate {
        /// Идентификатор участника-получателя.
        target_id: String,
        /// Строковое представление ICE-кандидата.
        candidate: String,
    },
}

/// Сообщение от сервера к клиенту.
///
/// # Типы сообщений
///
/// | Тип | Когда отправляется |
/// |-----|-------------------|
/// | `RoomCreated` | После успешного создания комнаты |
/// | `RoomJoined` | После успешного присоединения к комнате |
/// | `ParticipantJoined` | Когда новый участник входит в комнату (уведомление остальным) |
/// | `ParticipantLeft` | Когда участник покидает комнату (уведомление остальным) |
/// | `Offer`/`Answer`/`IceCandidate` | Ретрансляция WebRTC-сигналов между пирами |
/// | `Error` | При любой ошибке обработки запроса |
///
/// # Примеры JSON
///
/// ```json
/// {"type": "room_created", "room_id": "abc", "participant_id": "user-1"}
/// {"type": "participant_joined", "participant": {"id": "user-2", "display_name": "Bob"}}
/// {"type": "error", "message": "Room not found"}
/// ```
#[derive(Debug, Clone, Serialize, Deserialize)]
#[serde(tag = "type", rename_all = "snake_case")]
pub enum ServerMessage {
    /// Ответ на [`ClientMessage::CreateRoom`].
    ///
    /// Содержит сгенерированные идентификаторы для дальнейшего использования.
    RoomCreated {
        /// Уникальный идентификатор созданной комнаты.
        room_id: String,
        /// Идентификатор участника-создателя в этой комнате.
        participant_id: String,
    },

    /// Ответ на [`ClientMessage::JoinRoom`].
    ///
    /// Содержит информацию о комнате и текущих участниках.
    RoomJoined {
        /// Идентификатор комнаты, к которой присоединился участник.
        room_id: String,
        /// Список всех участников комнаты (включая текущего).
        participants: Vec<ParticipantInfo>,
        /// Идентификатор текущего участника (для удобства клиента).
        participant_id: String,
    },

    /// Уведомление всем участникам: в комнату вошёл новый участник.
    ///
    /// Отправляется всем, кроме вошедшего (ему приходит `RoomJoined`).
    ParticipantJoined {
        /// Информация о новом участнике.
        participant: ParticipantInfo,
    },

    /// Уведомление всем участникам: участник покинул комнату.
    ///
    /// Отправляется всем, кроме ушедшего (ему не нужно уведомление).
    ParticipantLeft {
        /// Идентификатор участника, который покинул комнату.
        participant_id: String,
    },

    /// Ретрансляция WebRTC Offer от одного участника другому.
    ///
    /// Сервер не интерпретирует содержимое `sdp`, только пересылает.
    Offer {
        /// Идентификатор участника-отправителя (инициатора).
        from_id: String,
        /// SDP-описание, полученное от отправителя.
        sdp: String,
    },

    /// Ретрансляция WebRTC Answer от одного участника другому.
    Answer {
        /// Идентификатор участника-отправителя (ответившего).
        from_id: String,
        /// SDP-описание, полученное от отправителя.
        sdp: String,
    },

    /// Ретрансляция WebRTC ICE Candidate от одного участника другому.
    IceCandidate {
        /// Идентификатор участника-отправителя.
        from_id: String,
        /// Строковое представление ICE-кандидата.
        candidate: String,
    },

    /// Сообщение об ошибке.
    ///
    /// Отправляется клиенту при любой проблеме обработки его запроса.
    /// Содержит человекочитаемое описание для отладки.
    Error {
        /// Описание ошибки (не предназначено для парсинга клиентом).
        message: String,
    },
}

/// Публичная информация об участнике комнаты.
///
/// Этот тип используется для передачи данных о участниках между клиентами.
/// Не содержит чувствительной или внутренней информации (например, каналов связи).
///
/// # Пример JSON
///
/// ```json
/// {"id": "user-123", "display_name": "Alice"}
/// ```
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ParticipantInfo {
    /// Уникальный идентификатор участника в рамках комнаты.
    pub id: String,

    /// Отображаемое имя участника.
    ///
    /// Может быть пустым или содержать значение по умолчанию ("Anonymous"),
    /// если клиент не указал имя при присоединении.
    #[serde(default = "default_display_name")]
    pub display_name: String,
}

// ─────────────────────────────────────────────────────────────
// Вспомогательные функции
// ─────────────────────────────────────────────────────────────

/// Возвращает имя участника по умолчанию.
///
/// Используется сердером через `#[serde(default = "...")]`,
/// если поле `display_name` отсутствует или равно `null`.
#[inline]
fn default_display_name() -> String {
    "Anonymous".to_string()
}

