//! Доменный слой: сущности и бизнес-правила.
//!
//! Этот модуль содержит чистую бизнес-логику приложения:
//! - Сущности ([`Room`], [`Participant`])
//! - Сообщения протокола ([`ClientMessage`], [`ServerMessage`])
//! - Доменные ошибки ([`DomainError`])
//!
//! # Принципы
//!
//! - ❌ Никаких зависимостей от внешних крейтов (кроме стандартной библиотеки и `serde`)
//! - ❌ Никаких знаний о транспорте, хранилищах или веб-фреймворках
//! - ✅ Только чистая логика: валидация, инварианты, правила бизнеса
//!
//! Это позволяет тестировать домен изолированно и легко заменять внешние адаптеры.

